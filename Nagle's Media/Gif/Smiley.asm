;Hexdump of smiley.gif with preprocessor magic to decode
%include "../MediaUtils.asm"

hdr: ;File header
 db 'GIF89a' ;0x00 Identifer
 dw 0x000F   ;0x06 Width
 dw 0x000F   ;0x08 Height
 db 0xF3     ;0x0A GCT flags
  ;0x07 Color table size 1<<(N+1)
  ;0x08 Color table sort
  ;0x70 Color bit depth
  ;0x80 Global color table flag
 db 0x00     ;0x0B Background color index
 db 0x00     ;0x0C Pixel ratio
gct: ;Global color table
 db 0x00,0x00,0x00 ;0x0D, Color 0x00
 db 0xCE,0xCE,0x29 ;0x10, Color 0x01
 db 0xD6,0xD6,0x29 ;0x13, Color 0x02
 db 0xE0,0xE0,0x33 ;0x16, Color 0x03
 db 0xF3,0xF3,0x3D ;0x19, Color 0x04
 db 0xFB,0xFB,0x4E ;0x1C, Color 0x05
 db 0xFF,0xFF,0x5E ;0x1F, Color 0x06
 db 0xFF,0xFF,0x73 ;0x22, Color 0x07
 db 0xFF,0xFF,0x7B ;0x25, Color 0x08
 db 0xFF,0xFF,0x94 ;0x28, Color 0x09
 db 0xFF,0xFF,0xAD ;0x2B, Color 0x0A
 db 0xFF,0xFF,0xC1 ;0x2E, Color 0x0B
 db 0xFF,0xFF,0xE7 ;0x31, Color 0x0C
 db 0x00,0x00,0x00 ;0x34, Color 0x0D
 db 0x00,0x00,0x00 ;0x37, Color 0x0E
 db 0x00,0x00,0x00 ;0x3A, Color 0x0F
gce: ;Graphics control extension
 db 0x21   ;0x3D Extension identifier
 db 0xF9   ;0x3E Graphic control identifer
 db 0x04   ;0x3F GCE size
 db 0x01   ;0x40 Transparent background color
 dw 0x0001 ;0x41 Animation delay in ms
 db 0x0D   ;0x43 Color number of transparent pixel
 db 0x00   ;0x44 GCE end
imd: ;Image descriptor
 db 0x2C       ;0x45 Identifer
 dd 0x00000000 ;0x46 Top left corner
 dd 0x000F000F ;0x4A Width and height
 db 0x00       ;0x4E Local color table bit
img: ;Image data
 %assign lzw_ents 0
 %macro lzw_ent 1-*
  %rep %0
  db %1
  %assign lzw%[lzw_ents] %1
  %assign lzw_ents lzw_ents+1
  %rotate 1
  %endrep
  %endmacro
 db 0x04 ;0x4F LZW minimum code size
 db 0x65 ;0x50 Bytes of encoded data
 lzw_ent 0xB0,0x49,0x09,0x6A,0x9D,0xB8,0x55,0x84,0x8E,0x31,0x17,0x06,0x24,0x24,0x89,0x18 ;0x51 0x00
 lzw_ent 0x05,0x01,0x4C,0x63,0x52,0x29,0x64,0x55,0x0C,0xAB,0xE6,0x2E,0x38,0x7C,0x02,0xB4 ;0x61 0x10
 lzw_ent 0x86,0x24,0x8A,0x05,0x63,0xA1,0x43,0x0D,0x04,0x1B,0x17,0x20,0x97,0x40,0x54,0x8E ;0x71 0x20
 lzw_ent 0x95,0xC3,0x4B,0x01,0x4B,0x78,0x2A,0x48,0x80,0xC1,0x59,0x6A,0xA2,0x78,0x81,0xCA ;0x81 0x30
 lzw_ent 0x07,0xC0,0xE9,0x7C,0x54,0x82,0xB0,0xA6,0x00,0xFA,0xA0,0x54,0x03,0xB5,0x86,0x50 ;0x91 0x40
 lzw_ent 0x28,0x58,0x9E,0x01,0xB9,0x66,0xC0,0xEF,0xA7,0xF5,0x14,0x00,0x02,0x83,0x79,0x21 ;0xA1 0x50
 lzw_ent 0x19,0x1A,0x77,0x19,0x11                                                        ;0xB1 0x60
 db 0x00 ;Subblock terminator
 db 0x3B ;File terminator

;LZW decode
 %assign bits 4           ;bit minimum size
 %assign bitc bits+1      ;bit current size
 %assign didx 0           ;Dictionary index
 %assign n    0           ;Current bit
 %assign code 0
 %assign idx  0
 %rep lzw_ents
  %assign byte lzw%[idx]
  %rep 8
   %assign bit   (byte &  0x01)        ;Get bit
   %assign code  (code  |  bit<<(n+1)) ;Set code[n] bit
   %assign byte  (byte >> 0x01)        ;Rot byte
   %assign n     n+1                   ;Inc n
   %if n == bitc                       ;Check if a code has been written
    %assign  code (code>>1) ;Remove the last shift
    ;hexprint code
    %if   didx == (1<<bitc)-1 ;Check bit increase
     %assign bitc bitc+1
     %endif
    %if   code == (1<<bits)) ;Check reset
     %assign bitc bits+1
     %assign didx (1<<bits)
    %elif code == (1<<bits)+1
    %elif code > (1<<bits)
     %define dict%[didx]
     %assign pre code-1
     %assign count dict%[pre]_ct
     %assign dict%[didx]_ct count+1
     %assign idx2 0
     %rep count
      %assign value dict%[pre]_%[idx2]
      %assign dict%[didx]_%[idx2] value
      hexprint value
      %assign idx2 idx2+1
      %endrep
     %assign value dict%[code]_0
     %assign dict%[didx]_%[count] value
     hexprint value
    %else
     %define dict%[didx]
     %assign dict%[didx]_ct 1
     %assign dict%[didx]_0 code
     hexprint code
     %endif
    %assign didx didx+1
    %assign code 0
    %assign n 0
    %endif
   %endrep
  %assign idx idx+1
  %endrep